export function t(key, params = null) {
    const lang = state.language || "en";
    let text = translations[lang]?.[key] || translations["en"][key] || key;
    if (params) {
        Object.keys(params).forEach(pKey => {
            text = text.replace(`{${pKey}}`, params[pKey]);
        });
    }
    return text;
}
